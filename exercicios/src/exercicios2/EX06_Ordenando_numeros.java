package exercicios2;

public class EX06_Ordenando_numeros {
    public static void main(String[] args) {
        int i = 0;
        for (i = 20; i >= 1; --i) {
            System.out.println(i);
        }
        System.out
                .println(
                        "\n-----------------------------------------------------------------------------------------------\n");
        for (i = 20; i >= 1; --i) {
            System.out.printf("%d - ", i);
        }
    }
}
